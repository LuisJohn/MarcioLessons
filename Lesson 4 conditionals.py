
# Hot => Wear Vests
# Cold => Wear Gloves
# Raining => Wear rain coats
# Sunny => Stay indoors

print("Whats is the weather condition?")

condition = "Snow"

if condition == "Sunny":
    print("Stay Indoors")

elif condition == "Cold":
    print("Wear Gloves")

elif condition == "Raining":
    print("Wear raincoats")

elif condition == "Hot":
    print("Wear Vests")

else:
    print("Condition is invalid")
